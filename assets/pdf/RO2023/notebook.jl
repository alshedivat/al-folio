# ╔═╡ cd25e880-630b-45ba-b72a-99da1008c311
function two_opt(solution::Solution, instance::Instance, i::Int, j::Int)
	@assert i < j "$i should be lower than $j"
	(; value) = solution
	new_sol = deepcopy(value)
	N = nbcity(instance)
	new_sol[i+1:j] = new_sol[j:-1:i+1]
	ci = value[i]
	ci_plus1 = value[i+1]
	cj = value[j]
	cj_plus1 = value[j < N ? j+1 : 1]
	d = instance.distance
	Δd = d[ci, cj] + d[ci_plus1, cj_plus1] - d[ci, ci_plus1] - d[cj, cj_plus1]
	return Solution(new_sol), Δd
end

# ╔═╡ 8f833885-4366-4618-a4f3-b7725b926f58
function two_opt(solution, instance)
	N = nbcity(instance)
	i = rand(1:N-2)
	j = rand(i+2:N)
	return two_opt(solution, instance, i, j)
end

