# ╔═╡ 529e6f0b-1c49-49fd-9789-dea7cdd8355d
# Create a random instance with `nb_cities` cities, in a square `ub` x `ub`.
function random_instance(nb_cities::Int, ub)
	cities = [random_point(ub) for _ in 1:nb_cities]
	mat = compute_distance_matrix(cities)
	return Instance(cities, mat)
end

