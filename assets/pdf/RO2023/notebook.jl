# ╔═╡ 280879de-d323-4787-a11f-87687904129a
struct Point
	x::Float64
	y::Float64
end

# ╔═╡ 676fad7e-8766-455b-bc13-ea04ffd2403f
# Compute euclidian distance between `p₁` and `p₂`.
distance(p₁::Point, p₂::Point) = sqrt((p₁.x - p₂.x)^2 + (p₁.y - p₂.y)^2)

# ╔═╡ b535bae4-5990-4f1d-bf97-0c68a205bbcc
# Draw uniformly a random point with coordinates between 0 and `ub`.
random_point(ub) = Point(rand() * ub, rand() * ub)

# ╔═╡ 287986ab-120b-4382-940c-9d21a7e5ab87
md"""
### 1.2 Instance encoding
