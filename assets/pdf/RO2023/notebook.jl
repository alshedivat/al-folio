# ╔═╡ 7cecf8e3-f386-4a3c-a559-e1858c578161
function greedy(instance)
	N = nbcity(instance)
	already_visited = falses(N)
	current_location = 1
	already_visited[current_location] = true
	sol = Int[current_location]
	for step in 2:N
		# Compute closest (non already visited) city to current location
		min_dist = Inf
		closest_city = -1
		for i in 1:N
			already_visited[i] && continue
			d = instance.distance[current_location, i]
			if d < min_dist
				min_dist = d
				closest_city = i
			end
		end
		push!(sol, closest_city)
		already_visited[closest_city] = true
		current_location = closest_city
	end
	return Solution(sol)
end

