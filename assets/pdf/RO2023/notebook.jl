# ╔═╡ 3056aec6-633e-11ed-3dcc-db9fbf97daca
struct Instance
	cities::Vector{Point}
	distance::Matrix{Float64}
end

# ╔═╡ 1b930e75-fc2b-44d1-bfa6-c9fe5cf5217b
nbcity(instance::Instance) = length(instance.cities)

# ╔═╡ 26bebf5e-33ee-4781-9a4d-6157c36922f5
# Function that precomputes the distance matrix
function compute_distance_matrix(cities::Vector{Point})
	N = length(cities)
	mat = zeros(N, N)
	for i in 1:N
		for j in i+1:N
			mat[i, j] = i == j ? 0.0 : distance(cities[i], cities[j])
			mat[j, i] = mat[i, j]
		end
	end
	return mat
end

# ╔═╡ b1321928-e79b-4457-949d-01fec117a5c7
