# ╔═╡ d4cf14f5-6411-498d-bc19-a4b168d9902e
function local_search(
	instance; nb_it, initializer, select_neighbor
)
	best_sol = initializer(instance)      # initialize a solution
	best_cost = cost(best_sol, instance)  # compute its cost
	@info "Initial cost: $best_cost"
	@progress for it in 1:nb_it
		# Select a random neighbor
		x, Δd = select_neighbor(best_sol, instance)

		# Accept the change if it improves the current best solution
		if Δd < 0
			best_sol = x
			best_cost += Δd
		end
	end
	return best_sol
end

