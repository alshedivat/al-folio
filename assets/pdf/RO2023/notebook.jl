function switch(solution, instance)
	N = nbcity(instance)
	i = rand(1:N-1)
	j = rand(i+1:N)
	val = deepcopy(solution.value)
	val[i] = solution.value[j]
	val[j] = solution.value[i]
	new_sol = Solution(val)
	Δd = cost(new_sol, instance) - cost(solution, instance)
	return new_sol, Δd
end

