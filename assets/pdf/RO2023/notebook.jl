# ╔═╡ 7cf451e9-6983-4723-896a-cb84e8935df7
function is_feasible(solution::Solution, instance::Instance)
	N = nbcity(instance)
	length(solution.value) == N || return false
	check = falses(N)
	for i in solution.value
		check[i] = true
	end
	return all(check)
end

# ╔═╡ ae74e749-0f0f-4953-a4ac-f5619c301c54
md"""
### 2.2 Evaluation function
"""

# ╔═╡ 0bd8ce15-641c-4823-8f11-b2df0de96501
function cost(solution::Solution, instance::Instance)
	N = nbcity(instance)
	(; distance) = instance
	(; value) = solution
	c = 0.0
	for i in 1:N
		c += distance[value[i], value[i < N ? i+1 : 1]]
	end
	return c
end

