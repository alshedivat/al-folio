# ╔═╡ 4d310f47-8c0c-4d88-b38f-5136599fdf3b
function simulated_annealing(
	instance;
	nb_it=100, initializer=greedy, select_neighbor=switch,
	T₀, T_min, α,
)
	best_sol = initializer(instance)
	best_cost = cost(best_sol, instance)
	current_sol = deepcopy(best_sol)
	current_cost = best_cost
	T = T₀
	@info "Initial cost: $best_cost"

	# Compute number of iterations needed (only needed for the progress bar)
	n = 0
	while T >= T_min
		n += 1
		T *= α
	end
	@info "Running $n simulated annealing steps"
	@progress for _ in 1:n
		for it in 1:nb_it
			# Select a random neighbor
			x, Δd = select_neighbor(current_sol, instance)

			# Accept the change if it improves the current best solution
			if Δd < 0
				current_sol = x
				current_cost += Δd
				# Store current solution if it is best
				if current_cost < best_cost
					best_cost = current_cost
					best_sol = deepcopy(current_sol)
				end
				continue
			end

			# Else, still accept on p
			p = exp(-Δd / T)
			if rand() <= p
				current_sol = x
				current_cost += Δd
			end
		end
		T *= α
	end
	return best_sol
end

